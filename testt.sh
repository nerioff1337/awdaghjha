#!/bin/bash

# Проверка на права root
if [ "$EUID" -ne 0 ]; then
  echo "❌ Ошибка: Запустите скрипт с правами root (sudo bash имя_скрипта)"
  exit 1
fi

echo "🚀 1. Настраиваем базовый фаервол UFW..."

# Разрешаем SSH, чтобы не потерять доступ
ufw allow 22/tcp
# Полный доступ для твоего домашнего IP
ufw allow from 195.122.224.226
# Доступ для балансировщика
ufw allow from 176.124.222.168 to any port 8443

# Включаем UFW
ufw --force enable

echo "🐳 2. Интегрируем UFW с правилами Docker..."

# Проверяем, не добавляли ли мы уже эти правила ранее (чтобы не дублировать)
if ! grep -q "DOCKER-USER" /etc/ufw/after.rules; then
  # Дописываем правила в конец конфигурации UFW
  cat <<EOT >> /etc/ufw/after.rules

# --- БЛОКИРОВКА ПОРТОВ DOCKER ДЛЯ ПОСТОРОННИХ ---
*filter
:DOCKER-USER - [0:0]
# Пускаем балансировщик
-A DOCKER-USER -s 176.124.222.168 -j RETURN
# Пускаем твой админский IP
-A DOCKER-USER -s 195.122.224.226 -j RETURN
# Блокируем всех остальных, кто ломится на порт 8443
-A DOCKER-USER -p tcp --dport 8443 -j DROP
# Разрешаем весь остальной легальный трафик контейнеров (например, их выход в интернет)
-A DOCKER-USER -j RETURN
COMMIT
# ------------------------------------------------
EOT
  echo "✅ Спец-правила для Docker успешно добавлены!"
else
  echo "⚠️ Правила для Docker уже были настроены ранее."
fi

# Перезагружаем UFW, чтобы новые правила вступили в силу
ufw reload

echo "🎉 Готово! Сервер забронирован. Трафик к MTProto теперь идет только через балансировщик."
